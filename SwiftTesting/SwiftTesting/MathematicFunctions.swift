import Foundation

class MathematicFunctions {
    
    func addIntegers(x: Int, y: Int) -> Int {
        return x + y
    }
    
    func multiplayNumbers(x: Int, y: Int) -> Int {
        return x * y
    }
    
    func divideIntegers(x: Int, y: Int) -> Int {
        return x / y
    }
    
}
